def encode(s=0, z=0, a=0, p=0, c=0):
    return(
        c << 0 |
        1 << 1 |
        p << 2 |
        a << 4 |
        z << 6 |
        s << 7
    )

def decode(flags):
    return(
        bool(flags & 0x80),
        bool(flags & 0x40),
        bool(flags & 0x10),
        bool(flags & 0x04),
        bool(flags & 0x01)
    )

def z(acc):
    return bool(acc == 0)

def s(acc):
    return bool(acc & 0x80)

def p(acc):
    return bool(__parity[acc])
